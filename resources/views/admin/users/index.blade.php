    <!-- JavaScript for Search Functionality -->
    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const searchInput = document.getElementById('searchInput');
            const table = document.getElementById('usersTable');
            const rows = table.getElementsByTagName('tbody')[0].getElementsByTagName('tr');
            const showingCount = document.getElementById('showingCount');

            searchInput.addEventListener('keyup', function() {
                const searchTerm = this.value.toLowerCase();
                let visibleRows = 0;

                for (let i = 0; i < rows.length; i++) {
                    const row = rows[i];
                    const name = row.getElementsByTagName('td')[0]?.textContent.toLowerCase() || '';
                    const email = row.getElementsByTagName('td')[1]?.textContent.toLowerCase() || '';
                    const role = row.getElementsByTagName('td')[2]?.textContent.toLowerCase() || '';

                    if (name.includes(searchTerm) || email.includes(searchTerm) || role.includes(searchTerm)) {
                        row.style.display = '';
                        visibleRows++;
                    } else {
                        row.style.display = 'none';
                    }
                }

                showingCount.textContent = visibleRows;
            });
        });
    </script>
